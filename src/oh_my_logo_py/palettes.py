def resolve_palette(name: str) -> list[str] | None:
    """パレット名から色のリストを取得します"""
    return PALETTES.get(name)


def get_palette_names() -> list[str]:
    """利用可能なパレット名の一覧を取得します"""
    return list(PALETTES.keys())


def get_default_palette() -> list[str]:
    """デフォルトのパレットを取得します"""
    return PALETTES["grad-blue"]


